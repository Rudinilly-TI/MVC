function CorUm(){
	var cor = document.getElementById('CorUm').value;
	if(cor == 1){
	document.getElementById("cor1").style.background = "black";
}else if(cor == 2){
	document.getElementById("cor1").style.background = "brown";
}else if(cor == 3){
	document.getElementById("cor1").style.background = "red";	
}
}
